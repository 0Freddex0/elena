// Inicializamos la variable de suma
var suma = 0;

// Iteramos sobre los números del 1 al 5
for (var i = 1; i <= 5; i++) {
    console.log("Número:", i); // Muestra el número actual
    suma += i; // Suma el número actual a la variable de suma
}

// Muestra la suma total de los números
console.log("La suma de los números del 1 al 5 es:", suma);
